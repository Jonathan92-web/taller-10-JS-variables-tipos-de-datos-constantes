let a = 10;
let b = 5;

// Suma
alert(`${a} + ${b} = ${a + b}`); // 15

// Resta
alert(`${a} - ${b} = ${a - b}`); // 5

// Multiplicación
alert(`${a} x ${b} = ${a * b}`); // 50

// División
alert(`${a} / ${b} = ${a / b}`); // 2

// Suma de sustituciones (también disponible para todos los demás operadores)
let X = 10;

X += 5;
document.write(`${X}<br>`); // 15

X *= 5;
document.write(`${X}<br>`); // 75

X /= 5;
document.write(`${X}`); // 15
