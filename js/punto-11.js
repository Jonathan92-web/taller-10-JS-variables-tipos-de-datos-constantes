const distanciaKm = 42;

// Convertir a metros (1 km = 1000 m)
const distanciaMetros = distanciaKm * 1000;

// Convertir a centímetros (1 m = 100 cm)
const distanciaCm = distanciaMetros * 100;

// Convertir a millas (1 km ≈ 0.621371 mi)
const distanciaMillas = distanciaKm * 0.621371;

alert(`Distancia en kilómetros: ${distanciaKm}`);
alert(`Distancia en metros: ${distanciaMetros}`);
alert(`Distancia en centímetros: ${distanciaCm}`);
alert(`Distancia en millas: ${distanciaMillas}`);
